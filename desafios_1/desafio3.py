#Obtener el promedio de 3 notas de alumnos.

alumno1= float(input("Ingrese la nota del primer alumno: "))
alumno2= float(input("Ingrese la nota del segundo alumno: "))
alumno3= float(input("Ingrese la nota del tercer alumno: "))

